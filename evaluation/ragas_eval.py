from ragas import evaluate
from ragas.metrics import (
    faithfulness,
    answer_relevancy,
    context_precision,
    context_recall,
)
from ragas.llms import LangchainLLMWrapper
from ragas.embeddings import LangchainEmbeddingsWrapper
from langchain_anthropic import ChatAnthropic
from langchain_community.embeddings import HuggingFaceEmbeddings
from datasets import Dataset
import structlog

from config.config import settings

logger = structlog.get_logger()


class RAGASEvaluator:
    """RAGAS evaluation suite for LexPilot RAG pipeline."""

    def __init__(self):
        self.llm = LangchainLLMWrapper(
            ChatAnthropic(
                model="claude-sonnet-4-20250514",
                api_key=settings.anthropic_api_key,
            )
        )
        self.embeddings = LangchainEmbeddingsWrapper(
            HuggingFaceEmbeddings(model_name=settings.embedding_model)
        )
        self.metrics = [
            faithfulness,
            answer_relevancy,
            context_precision,
            context_recall,
        ]

    async def evaluate_dataset(self, eval_data: list[dict]) -> dict:
        """Evaluate on a dataset of {question, answer, contexts, ground_truth}.

        Args:
            eval_data: List of dicts with keys:
                - question: str
                - answer: str (generated by LexPilot)
                - contexts: list[str] (retrieved chunks)
                - ground_truth: str (expected answer)
        """
        dataset = Dataset.from_list(eval_data)

        result = evaluate(
            dataset=dataset,
            metrics=self.metrics,
            llm=self.llm,
            embeddings=self.embeddings,
        )

        scores = {
            "faithfulness": float(result["faithfulness"]),
            "answer_relevancy": float(result["answer_relevancy"]),
            "context_precision": float(result["context_precision"]),
            "context_recall": float(result["context_recall"]),
        }

        logger.info("ragas_eval_complete", **scores)
        return scores

    def check_thresholds(self, scores: dict) -> dict:
        """Check if scores meet minimum thresholds. Returns pass/fail per metric."""
        thresholds = {
            "faithfulness": 0.80,
            "context_precision": 0.70,
            "answer_relevancy": 0.75,
        }
        results = {}
        for metric, threshold in thresholds.items():
            score = scores.get(metric, 0)
            results[metric] = {
                "score": score,
                "threshold": threshold,
                "passed": score >= threshold,
            }
        return results
